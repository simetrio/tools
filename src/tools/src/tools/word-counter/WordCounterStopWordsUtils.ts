export const WordCounterStopWordsUtils = {
    is: (value: string): boolean => isStopWord(value),
};

function isStopWord(value: string): boolean {
    if (!stopWords.size) {
        fillStopWords();
    }

    return !!stopWords.get(value);
}

function fillStopWords(): void {
    addStopWords(split(english, "\n"));
    addStopWords(split(russian, "\n"));
}

function addStopWords(value: string[]): void {
    value.forEach((x) => stopWords.set(x, true));
}

function split(value: string, char: string): string[] {
    return value
        .split(char)
        .map((x) => x.trim())
        .filter((x) => x !== "");
}

